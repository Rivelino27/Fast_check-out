let currentLang = localStorage.getItem('fcLang') || 'pt';

function t(key) {
  return TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.pt[key] ?? key;
}

const CAT_KEYS = {
  '': 'cat_geral', 'diarias': 'cat_diarias', 'frigobar': 'cat_frigobar',
  'room_service': 'cat_room_service', 'lavanderia': 'cat_lavanderia',
  'restaurante': 'cat_restaurante', 'outros': 'cat_outros',
};
function tCat(cat) { return t(CAT_KEYS[cat] ?? 'cat_geral'); }

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('fcLang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(b =>
    b.classList.toggle('active', b.dataset.lang === lang));
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key  = el.dataset.i18n;
    const attr = el.dataset.i18nAttr;
    if (attr) el.setAttribute(attr, t(key));
    else      el.innerHTML = t(key);
  });
}
